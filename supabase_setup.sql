-- 3. RLS Policies

-- Users: Read all users. Update own profile.
CREATE POLICY "Anyone can read users" ON users FOR SELECT USING (true);
CREATE POLICY "Users can update own profile" ON users FOR UPDATE USING (auth.uid() = id);

-- Clubs: Anyone can read
CREATE POLICY "Anyone can read clubs" ON clubs FOR SELECT USING (true);
CREATE POLICY "Admin can manage clubs" ON clubs FOR ALL USING (
  EXISTS (SELECT 1 FROM users WHERE users.id = auth.uid() AND role = 'admin')
);

-- Club Organisers: Read all. Admin updates.
CREATE POLICY "Anyone can read club_organisers" ON club_organisers FOR SELECT USING (true);
CREATE POLICY "Admin can approve club_organisers" ON club_organisers FOR UPDATE USING (
  EXISTS (SELECT 1 FROM users WHERE users.id = auth.uid() AND role = 'admin')
);
CREATE POLICY "Admin can manage club_organisers" ON club_organisers FOR ALL USING (
  EXISTS (SELECT 1 FROM users WHERE users.id = auth.uid() AND role = 'admin')
);
CREATE POLICY "Users can request to be organiser" ON club_organisers FOR INSERT WITH CHECK (
  auth.uid() = user_id
);

-- Events: Read all. Organisers insert/update.
CREATE POLICY "Anyone can read events" ON events FOR SELECT USING (true);
CREATE POLICY "Approved organisers can insert events" ON events FOR INSERT WITH CHECK (
  EXISTS (SELECT 1 FROM club_organisers WHERE club_id = events.club_id AND user_id = auth.uid() AND approved = true)
);
CREATE POLICY "Approved organisers can update events" ON events FOR UPDATE USING (
  EXISTS (SELECT 1 FROM club_organisers WHERE club_id = events.club_id AND user_id = auth.uid() AND approved = true)
);

-- Registrations: Read own or if organiser. Insert/update own.
CREATE POLICY "Users can read own registrations" ON registrations FOR SELECT USING (auth.uid() = student_id);
CREATE POLICY "Organisers can read event registrations" ON registrations FOR SELECT USING (
  EXISTS (SELECT 1 FROM events e JOIN club_organisers co ON e.club_id = co.club_id WHERE e.id = registrations.event_id AND co.user_id = auth.uid() AND co.approved = true)
);
CREATE POLICY "Users can register themselves" ON registrations FOR INSERT WITH CHECK (auth.uid() = student_id);
CREATE POLICY "Users can manage own registration" ON registrations FOR UPDATE USING (auth.uid() = student_id);

-- Follows: Read all. Insert/Update own.
CREATE POLICY "Anyone can read follows" ON follows FOR SELECT USING (true);
CREATE POLICY "Users can follow" ON follows FOR INSERT WITH CHECK (auth.uid() = student_id);
CREATE POLICY "Users can unfollow" ON follows FOR DELETE USING (auth.uid() = student_id);

-- Going Out Posts: Read all. Insert own.
CREATE POLICY "Anyone can read going_out_posts" ON going_out_posts FOR SELECT USING (true);
CREATE POLICY "Users can post" ON going_out_posts FOR INSERT WITH CHECK (auth.uid() = posted_by);
CREATE POLICY "Users can manage own posts" ON going_out_posts FOR ALL USING (auth.uid() = posted_by);

-- Going Out Interests: Read all. Insert own.
CREATE POLICY "Anyone can read going_out_interests" ON going_out_interests FOR SELECT USING (true);
CREATE POLICY "Users can add interest" ON going_out_interests FOR INSERT WITH CHECK (auth.uid() = student_id);
CREATE POLICY "Users can remove interest" ON going_out_interests FOR DELETE USING (auth.uid() = student_id);

-- Team Posts: Read all. Insert/Manage own.
CREATE POLICY "Anyone can read team_posts" ON team_posts FOR SELECT USING (true);
CREATE POLICY "Users can create team posts" ON team_posts FOR INSERT WITH CHECK (auth.uid() = posted_by);
CREATE POLICY "Users can manage own team posts" ON team_posts FOR ALL USING (auth.uid() = posted_by);

-- Team Applications: Read own or if post owner. Insert own.
CREATE POLICY "Users can read own applications" ON team_applications FOR SELECT USING (auth.uid() = student_id);
CREATE POLICY "Post owners can read applications" ON team_applications FOR SELECT USING (
  EXISTS (SELECT 1 FROM team_posts tp WHERE tp.id = team_applications.team_post_id AND tp.posted_by = auth.uid())
);
CREATE POLICY "Users can apply" ON team_applications FOR INSERT WITH CHECK (auth.uid() = student_id);
CREATE POLICY "Users can manage own application" ON team_applications FOR UPDATE USING (auth.uid() = student_id);
CREATE POLICY "Users can delete own application" ON team_applications FOR DELETE USING (auth.uid() = student_id);

-- Projects: Read all. Insert/Manage own.
CREATE POLICY "Anyone can read projects" ON projects FOR SELECT USING (true);
CREATE POLICY "Users can create projects" ON projects FOR INSERT WITH CHECK (auth.uid() = posted_by);
CREATE POLICY "Users can manage own projects" ON projects FOR ALL USING (auth.uid() = posted_by);

-- Announcements: Read all. Organiser Insert/Update.
CREATE POLICY "Anyone can read announcements" ON announcements FOR SELECT USING (true);
CREATE POLICY "Approved organisers can manage announcements" ON announcements FOR ALL USING (
  EXISTS (SELECT 1 FROM club_organisers WHERE club_id = announcements.club_id AND user_id = auth.uid() AND approved = true)
);

-- Notifications: Read/Update own.
CREATE POLICY "Users can read own notifications" ON notifications FOR SELECT USING (auth.uid() = user_id);
CREATE POLICY "Users can update own notifications" ON notifications FOR UPDATE USING (auth.uid() = user_id);


-- 4. Auth Hook & Trigger for Users signup
-- Supabase enforces email restrictions usually in the Dashboard (Authentication -> Providers -> Email Domains).
-- If you want a database trigger to insert a role:

CREATE OR REPLACE FUNCTION public.handle_new_user() 
RETURNS TRIGGER AS $$
BEGIN
  -- Prevent non SRM emails (redundant safety)
  IF NEW.email NOT LIKE '%@srmist.edu.in' THEN
    RAISE EXCEPTION 'Only @srmist.edu.in emails are allowed';
  END IF;

  INSERT INTO public.users (id, email, full_name, reg_number, branch, year, role)
  VALUES (
    NEW.id,
    NEW.email,
    NEW.raw_user_meta_data->>'full_name',
    NEW.raw_user_meta_data->>'reg_number',
    NEW.raw_user_meta_data->>'branch',
    (NEW.raw_user_meta_data->>'year')::integer,
    'student'
  );
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE PROCEDURE public.handle_new_user();


