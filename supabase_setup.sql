CREATE POLICY "Avatar images are publicly accessible" ON storage.objects FOR SELECT USING (bucket_id = 'avatars');
CREATE POLICY "Anyone can upload an avatar" ON storage.objects FOR INSERT WITH CHECK (bucket_id = 'avatars' AND auth.role() = 'authenticated');
CREATE POLICY "Anyone can update own avatar" ON storage.objects FOR UPDATE USING (auth.uid() = owner) WITH CHECK (bucket_id = 'avatars');

CREATE POLICY "Club assets are publicly accessible" ON storage.objects FOR SELECT USING (bucket_id = 'club-assets');
CREATE POLICY "Organisers can upload club assets" ON storage.objects FOR INSERT WITH CHECK (
  bucket_id = 'club-assets' AND 
  EXISTS (SELECT 1 FROM club_organisers WHERE user_id = auth.uid() AND approved = true)
);

CREATE POLICY "Event banners are publicly accessible" ON storage.objects FOR SELECT USING (bucket_id = 'event-banners');
CREATE POLICY "Organisers can upload event banners" ON storage.objects FOR INSERT WITH CHECK (
  bucket_id = 'event-banners' AND 
  EXISTS (SELECT 1 FROM club_organisers WHERE user_id = auth.uid() AND approved = true)
);
